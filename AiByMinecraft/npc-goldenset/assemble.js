  s+=`새로 보거나 들어 알게 된 게 있으면 <NPC_LEARN>한 줄 요약</NPC_LEARN>로 기억해 둬라(비공개 — 다음에 떠올려 쓰거나 전할 수 있다). 네가 아는 걸 말로 전할 수도 있으나(응답 순서 5), '확신'이 아니면 신뢰도에 맞춰 조심히(틀릴 수 있다고).\n`;
