    /** 마크다운 제거 후 JSON 객체 파싱. 실패/오류 응답이면 null. */
    /** 분할 생성 실패 진단 문자열 — 응답 길이와 끝부분(잘림 여부 판별용). */
    private String parseDiag(String raw) {
        if (raw == null) return " | (빈 응답)";
        int len = raw.length();
        String tail = raw.substring(Math.max(0, len - 100)).replaceAll("\\s+", " ");
        return " | 응답길이=" + len + "자 | 끝부분=" + tail;
    }

    private JsonObject tryParseObject(String raw) {
        if (raw == null || raw.startsWith("§c")) return null;
        try {
            JsonElement el = gson.fromJson(stripMarkdown(raw), JsonElement.class);
            return (el != null && el.isJsonObject()) ? el.getAsJsonObject() : null;
        } catch (Exception e) { return null; }
    }

    /** 응답에서 지정 key의 배열을 추출. {"key":[...]} 와 바로 [...] 둘 다 허용. 실패 시 null. */
    private JsonArray tryParseArray(String raw, String key) {
        if (raw == null || raw.startsWith("§c")) return null;
        try {
            JsonElement el = gson.fromJson(stripMarkdown(raw), JsonElement.class);
            if (el == null) return null;
            if (el.isJsonArray()) return el.getAsJsonArray();          // 모델이 배열만 출력
            if (el.isJsonObject()) {
                JsonObject o = el.getAsJsonObject();
                if (o.has(key) && o.get(key).isJsonArray()) return o.getAsJsonArray(key);
            }
        } catch (Exception ignore) { }
        return null;
    }

    /**
     * 청크(배열) 호출 + 파싱. 파싱 실패(또는 빈 배열)면 retriesLeft만큼 같은 프롬프트로 재호출한다.
     * AI가 가끔 JSON을 살짝 깨뜨려도, 값비싼 단일 생성 폴백으로 가기 전에 저렴하게 한 번 더 시도한다.
     * 끝내 실패하면 null을 반환(호출부에서 단일 생성 폴백).
     */
    private CompletableFuture<JsonArray> chunkArray(String prompt, String key, int retriesLeft) {
        return aiManager.callGmAiLarge(GDAM_SYSTEM_PROMPT, prompt).thenCompose(raw -> {
            JsonArray arr = tryParseArray(raw, key);
            if ((arr != null && arr.size() > 0) || retriesLeft <= 0)
                return CompletableFuture.completedFuture(arr);
            logger.warning("[gdam] " + key + " 청크 파싱 실패 → 재시도(" + retriesLeft + "회 남음)" + parseDiag(raw));
            return chunkArray(prompt, key, retriesLeft - 1);
        });
    }

    /** core에서 지정 필드(+선택적 roles)만 뽑아 컨텍스트용 JSON 문자열로 직렬화 */
    private String contextJson(JsonObject core, JsonArray roles, String... coreKeys) {
        JsonObject ctx = new JsonObject();
        for (String k : coreKeys) if (core.has(k)) ctx.add(k, core.get(k));
        if (roles != null) ctx.add("roles", roles);
        return gson.toJson(ctx);
    }

