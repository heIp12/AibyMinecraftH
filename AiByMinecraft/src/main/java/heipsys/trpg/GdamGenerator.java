    /** AI 선택 실패 시 큐레이션 목록에서 1개 사용 (안전 폴백). 필터(scp/urban)면 목록도 거른다. */
    private String fallbackFamiliarConcept(int roomNumber, String filter) {
        java.util.List<FamiliarEntity> pool = FAMILIAR_ENTITIES;
        if ("scp".equals(filter)) {
            var f = FAMILIAR_ENTITIES.stream().filter(e -> e.origin().contains("SCP")).toList();
            if (!f.isEmpty()) pool = f;
        } else if ("urban".equals(filter)) {
            var f = FAMILIAR_ENTITIES.stream().filter(e -> !e.origin().contains("SCP")).toList();
            if (!f.isEmpty()) pool = f;
        } else if ("japan".equals(filter)) {
            var f = FAMILIAR_ENTITIES.stream()
                .filter(e -> e.origin().contains("일본") || e.origin().toLowerCase().contains("japan")).toList();
            if (!f.isEmpty()) pool = f;
        } else if ("projectmoon".equals(filter)) {
            var f = FAMILIAR_ENTITIES.stream().filter(e -> e.origin().contains("프로젝트 문") || e.origin().contains("로보토미")).toList();
            if (!f.isEmpty()) pool = f;
        } else if ("game".equals(filter)) {
            var f = FAMILIAR_ENTITIES.stream().filter(e -> e.origin().contains("게임")).toList();
            if (!f.isEmpty()) pool = f;
        }
        FamiliarEntity entity = pool.get(
            java.util.concurrent.ThreadLocalRandom.current().nextInt(pool.size()));
        recordFamiliar(entity.name());
        String lore = "이름: " + entity.name() + "\n출처: " + entity.origin() + "\n" + entity.lore();
        return wrapFamiliarConcept(lore, roomNumber, filter);
    }

