    /** 스테이지에 맞춘 프로젝트 문 위험 등급대(−1 ~ +2 범위). */
    private static String pmGradeBand(int roomNumber) {
        String[] G = {"ZAYIN", "TETH", "HE", "WAW", "ALEPH"};
        int base = Math.max(1, Math.min(5, roomNumber));
        int lo = Math.max(1, base - 1), hi = Math.min(5, base + 2);
        StringBuilder b = new StringBuilder();
        for (int i = lo; i <= hi; i++) { if (b.length() > 0) b.append("·"); b.append(G[i - 1]); }
        return "이번 스테이지 중심 등급=" + G[base - 1] + ", 가능 범위 " + b + "(저등급 다수+고등급 소수)";
    }

