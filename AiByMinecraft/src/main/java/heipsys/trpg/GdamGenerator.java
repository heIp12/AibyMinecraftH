    private boolean validate(JsonObject g) {
        if (!g.has("entity"))   { logger.warning("[gdam] 검증 실패: entity 없음"); return false; }
        if (!g.has("timeline")) { logger.warning("[gdam] 검증 실패: timeline 없음"); return false; }
        if (!g.has("roles"))    { logger.warning("[gdam] 검증 실패: roles 없음"); return false; }

        JsonObject entity = g.getAsJsonObject("entity");
        if (!entity.has("name")) { logger.warning("[gdam] 검증 실패: entity.name 없음"); return false; }
        // ★ 해결형(weakness/solution/collapse) 또는 순수 회피형(escape) 중 최소 하나만 있으면 통과.
        //   (엘리베이터 게임 等 '파괴 불가·탈출형' 괴담은 solution/weakness가 비고 escape만 있을 수 있음 — line 117·290)
        boolean canResolve = nonBlank(entity, "solution") || nonBlank(entity, "weakness")
            || nonBlank(entity, "escape")
            || (g.has("world_rules") && g.get("world_rules").isJsonObject()
                && nonBlank(g.getAsJsonObject("world_rules"), "collapse_condition"));
        if (!canResolve) {
            logger.warning("[gdam] 검증 실패: 해결·회피 경로 모두 없음(solution/weakness/escape/collapse 중 하나 필요)");
            return false;
        }

        JsonObject tl = g.getAsJsonObject("timeline");
        // ★ 가변 단계 수(P66/CODE-17): 고정 4단계 강제 폐기 — 1부터 연속 최소 2단계만 필수(규모별 2~8).
        if (!tl.has("1") || !tl.has("2")) { logger.warning("[gdam] 검증 실패: timeline 단계 부족(최소 2단계)"); return false; }

        // 만남 가능성 검증
        if (g.has("meeting_design")) {
            JsonObject md = g.getAsJsonObject("meeting_design");
            if (md.has("physically_possible") && !md.get("physically_possible").getAsBoolean()) {
                logger.warning("[gdam] 검증 실패: meeting_design.physically_possible=false");
                return false;
            }
        }
        return true;
    }

    /** JsonObject에 key가 있고 값이 비어있지 않은가(문자열 기준). */
    private static boolean nonBlank(JsonObject o, String key) {
        if (o == null || !o.has(key) || o.get(key).isJsonNull()) return false;
        try { return !o.get(key).getAsString().isBlank(); }
        catch (Exception e) { return true; } // 비문자열(객체·배열)이면 존재로 인정
    }

    private String generateSeed() {
        String chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
        Random rng = new Random();
        StringBuilder sb = new StringBuilder("#");
        for (int i = 0; i < 4; i++) sb.append(chars.charAt(rng.nextInt(chars.length())));
        sb.append("-");
        for (int i = 0; i < 4; i++) sb.append(chars.charAt(rng.nextInt(chars.length())));
        return sb.toString();
    }

