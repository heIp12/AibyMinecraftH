    /** 분할 생성 사용 여부. 큰 JSON 한 방 출력의 잘림 위험을 줄이려 코어→배역→아이템 3단계로 나눈다.
     *  어느 단계든 실패하면 기존 단일 호출 생성으로 자동 폴백하므로 안전하다. */
    private static final boolean SPLIT_GENERATION = true;

    public CompletableFuture<JsonObject> generate(int roomNumber) {
        return generate(roomNumber, null);
    }

    /**
     * @param progress 단계 완료 시 호출되는 콜백 (null 허용).
     *   발화 값: "컨셉" / "구조" / "배역" / "아이템" / "저장"
     *   콜백은 비동기 스레드에서 호출되므로 Bukkit API 호출은 runTask로 감싸야 한다.
     */
    public CompletableFuture<JsonObject> generate(int roomNumber, Consumer<String> progress) {
        return generateEntityConcept()
            .exceptionally(ex -> "")
            .thenCompose(concept -> {
                if (progress != null) progress.accept("컨셉");
                String c = sanitizeConcept(concept);
                return SPLIT_GENERATION ? generateChunked(roomNumber, c, progress) : generate(roomNumber, 0, c, progress);
            });
    }

    /** 친숙한 친구들 모드용 생성(필터 미지정 = 모두 무작위). */
    public CompletableFuture<JsonObject> generate(int roomNumber, boolean familiar, Consumer<String> progress) {
        return generate(roomNumber, familiar, "random", progress);
    }

    /** 친숙한 친구들 모드 + 괴담 범위 필터. familiar=false면 일반 AI 창작 모드로 위임. */
    public CompletableFuture<JsonObject> generate(int roomNumber, boolean familiar, String filter, Consumer<String> progress) {
        return generate(roomNumber, familiar, filter, progress, null);
    }

    /**
     * returningCast: 피날레(마지막 스테이지)에서 '원년 배역'을 다시 캐스트로 쓰도록 하는 힌트.
     * 컨셉에 덧붙여 roles 청크가 이 인물들을 배역으로 생성하게 한다(생성 하위 메서드는 변경 없이 증강된 컨셉만 받음).
     */
    public CompletableFuture<JsonObject> generate(int roomNumber, boolean familiar, String filter,
                                                  Consumer<String> progress, String returningCast) {
        if (!familiar) {
            return generateEntityConcept().exceptionally(ex -> "").thenCompose(concept -> {
                if (progress != null) progress.accept("컨셉");
                String c = appendReturningCast(sanitizeConcept(concept), returningCast);
                return SPLIT_GENERATION ? generateChunked(roomNumber, c, progress) : generate(roomNumber, 0, c, progress);
            });
        }
        return generateFamiliarConcept(roomNumber, filter).thenCompose(concept -> {
            logger.info("[gdam] 친숙 모드(" + filter + ") 컨셉 생성 완료"); // ★스포 방지 — 괴담 이름은 로그에 남기지 않음
            if (progress != null) progress.accept("컨셉");
            String c = appendReturningCast(concept, returningCast);
            return SPLIT_GENERATION ? generateChunked(roomNumber, c, progress) : generate(roomNumber, 0, c, progress);
        });
    }

    /** 피날레 복귀 캐스트 지시를 컨셉 뒤에 덧붙인다(없으면 그대로). roles 청크가 head(=컨셉 포함)로 이를 본다. */
    private String appendReturningCast(String concept, String returningCast) {
        if (returningCast == null || returningCast.isBlank()) return concept;
        return concept
            + "\n\n## ★복귀 캐스트 (피날레) — 아래 인물들을 이번 시나리오 roles(배역)로 사용\n"
            + returningCast
            + "\n이 인물들이 ★다시 모여★ 최후의 사건을 맞는다. roles 배열의 char_name·성별·나이·직업을 "
            + "위 인물과 일치시키고(각 1명씩), 관계·단서·배경을 이들이 함께 겪는 결말로 엮어라. "
            + "새 인물을 창작하기보다 이 캐스트를 우선 배역으로 삼아라.";
    }

