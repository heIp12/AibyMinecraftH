    public void save(String seed, JsonObject gdam) throws Exception {
        File file = new File(gdamDir, seed + ".gdam");
        String encrypted = encrypt(gdam.toString());
        try (PrintWriter pw = new PrintWriter(new FileWriter(file))) {
            pw.println(seed);
            pw.print(encrypted);
        }
    }

    /** 저장된 씨드 목록 반환 (.gdam 암호화본 + 사용자가 넣은 평문 .json, 중복 제거) */
    public List<String> listSavedSeeds() {
        File[] files = gdamDir.listFiles((d, name) -> name.endsWith(".gdam") || name.endsWith(".json"));
        if (files == null) return new ArrayList<>();
        java.util.Arrays.sort(files, (a, b) -> Long.compare(b.lastModified(), a.lastModified())); // 최근 수정 순(최신 먼저)
        java.util.LinkedHashSet<String> seeds = new java.util.LinkedHashSet<>(); // 순서 유지 + 중복 제거
        for (File f : files) {
            String name = f.getName();
            int dot = name.lastIndexOf('.');
            if (dot > 0) seeds.add(name.substring(0, dot));
        }
        return new ArrayList<>(seeds);
    }

    /**
     * 복호화한 내용을 들여쓰기된 JSON 파일로 내보낸다.
     * 원본 .gdam 파일은 변경하지 않는다.
     *
     * @return 생성된 .json 파일의 절대 경로, 실패 시 null
     */
    public String exportJson(String seed) {
        JsonObject gdam = load(seed);
        if (gdam == null) return null;

        Gson pretty = new GsonBuilder().setPrettyPrinting().create();
        String json  = pretty.toJson(gdam);

        File out = new File(gdamDir, seed + ".json");
        try (PrintWriter pw = new PrintWriter(new FileWriter(out))) {
            pw.print(json);
            return out.getAbsolutePath();
        } catch (Exception e) {
            logger.warning("[GdamGenerator] JSON 내보내기 실패 (" + seed + "): " + e.getMessage());
            return null;
        }
    }

    /**
     * 항상 원본 로드 — 절대 수정하지 않음.
     * 세 형식을 모두 지원한다:
     *   ① 암호화 .gdam (플러그인 생성·저장: "seed\n<base64>")
     *   ② 평문 .gdam (사용자가 직접 작성/붙여넣은 통짜 JSON)
     *   ③ 평문 .json (클로드 프롬프트 등이 내보낸 시나리오 — .gdam이 없을 때 사용)
     * ②③은 LLM이 암호화할 수 없으므로 평문 그대로 받아 검증만 한다(스포일러 우려 없음: 사용자가 만든 파일).
     */
    public JsonObject load(String seed) {
        try {
            File gdamFile = new File(gdamDir, seed + ".gdam");
            File jsonFile = new File(gdamDir, seed + ".json");
            // ③ 평문 JSON(.json) — .gdam이 없을 때만(있으면 원본 .gdam 우선)
            if (!gdamFile.exists() && jsonFile.exists()) {
                JsonObject g = gson.fromJson(Files.readString(jsonFile.toPath()), JsonObject.class);
                return (g != null && validate(g)) ? g : null;
            }
            if (!gdamFile.exists()) return null;
            String content = Files.readString(gdamFile.toPath()).trim();
            // ② 평문 .gdam — 통째로 JSON이면 복호화 없이 그대로 파싱
            if (content.startsWith("{")) {
                JsonObject g = gson.fromJson(content, JsonObject.class);
                return (g != null && validate(g)) ? g : null;
            }
            // ① 암호화 .gdam — 첫 줄 seed, 둘째 줄 base64(IV+ciphertext)
            List<String> lines = Files.readAllLines(gdamFile.toPath());
            if (lines.size() < 2) return null;
            String decrypted = decrypt(lines.get(1));
            return gson.fromJson(decrypted, JsonObject.class);
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }

