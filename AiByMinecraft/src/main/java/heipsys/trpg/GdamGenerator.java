    private String encrypt(String plaintext) throws Exception {
        Cipher cipher = Cipher.getInstance("AES/GCM/NoPadding");
        byte[] iv = new byte[12];
        new SecureRandom().nextBytes(iv);
        cipher.init(Cipher.ENCRYPT_MODE, SHARED_KEY, new GCMParameterSpec(128, iv)); // 공유 키로 암호화 → 서버 간 공유 가능
        byte[] enc = cipher.doFinal(plaintext.getBytes(StandardCharsets.UTF_8));

        byte[] combined = new byte[iv.length + enc.length];
        System.arraycopy(iv, 0, combined, 0, iv.length);
        System.arraycopy(enc, 0, combined, iv.length, enc.length);
        return Base64.getEncoder().encodeToString(combined);
    }

    private String decrypt(String base64) throws Exception {
        byte[] combined = Base64.getDecoder().decode(base64.trim());
        byte[] iv  = Arrays.copyOfRange(combined, 0, 12);
        byte[] enc = Arrays.copyOfRange(combined, 12, combined.length);
        // 공유 키 우선(서버 간 공유본) → 실패 시 설치별 키(레거시·기존 로컬 생성본)로 폴백.
        try { return decryptWith(SHARED_KEY, iv, enc); }
        catch (Exception e) { return decryptWith(aesKey, iv, enc); }
    }

    private static String decryptWith(SecretKey key, byte[] iv, byte[] enc) throws Exception {
        Cipher cipher = Cipher.getInstance("AES/GCM/NoPadding");
        cipher.init(Cipher.DECRYPT_MODE, key, new GCMParameterSpec(128, iv));
        return new String(cipher.doFinal(enc), StandardCharsets.UTF_8);
    }

    /** 모든 설치에서 동일한 공유 키 — 고정 문구의 SHA-256(32바이트). 시나리오 공유를 위해 같은 키를 쓴다. */
    private static SecretKey deriveSharedKey() {
        try {
            byte[] h = MessageDigest.getInstance("SHA-256")
                .digest("AiByMinecraft::gdam::shared-scenario-key::v1".getBytes(StandardCharsets.UTF_8));
            return new SecretKeySpec(h, "AES");
        } catch (Exception e) {
            throw new RuntimeException("공유 키 생성 실패", e);
        }
    }

    private SecretKey loadOrCreateKey(Plugin plugin) {
        File keyFile = new File(plugin.getDataFolder(), "trpg.key");
        try {
            if (keyFile.exists()) {
                byte[] bytes = Files.readAllBytes(keyFile.toPath());
                if (bytes.length != 32) {
                    // 손상된 키 파일 → 재생성
                    keyFile.delete();
                } else {
                    return new SecretKeySpec(bytes, "AES");
                }
            }
            KeyGenerator kg = KeyGenerator.getInstance("AES");
            kg.init(256, new SecureRandom());
            SecretKey key = kg.generateKey();
            plugin.getDataFolder().mkdirs();
            Files.write(keyFile.toPath(), key.getEncoded());
            return key;
        } catch (Exception e) {
            throw new RuntimeException("AES 키 초기화 실패", e);
        }
    }
