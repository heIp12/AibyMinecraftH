    private String stripMarkdown(String raw) {
        // 마크다운 코드블록 제거
        String s = raw.replaceAll("(?s)```json\\s*", "").replaceAll("```", "").trim();
        // 첫 { 부터 마지막 } 까지 추출 (앞뒤 설명 텍스트 제거)
        int start = s.indexOf('{');
        int end   = s.lastIndexOf('}');
        if (start != -1 && end != -1 && start < end) return s.substring(start, end + 1);
        return s;
    }

