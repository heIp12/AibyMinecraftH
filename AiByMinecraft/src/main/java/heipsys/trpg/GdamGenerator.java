    private File familiarHistoryFile() { return new File(gdamDir, ".familiar_history"); }

    /** 친숙 괴담 사용 기록을 파일에서 불러온다(최근 FAMILIAR_HISTORY_MAX개 유지). */
    private void loadFamiliarHistory() {
        File f = familiarHistoryFile();
        if (!f.exists()) return;
        try {
            java.util.List<String> lines = java.nio.file.Files.readAllLines(f.toPath());
            synchronized (familiarHistory) {
                familiarHistory.clear();
                for (String l : lines) { String t = l.trim(); if (!t.isEmpty()) familiarHistory.add(t); }
                while (familiarHistory.size() > FAMILIAR_HISTORY_MAX) familiarHistory.remove(0);
            }
        } catch (Exception ignored) {}
    }

    /** 사용한 괴담 이름을 기록하고(최신화) 파일에 저장한다. */
    private void recordFamiliar(String name) {
        if (name == null || name.isBlank()) return;
        synchronized (familiarHistory) {
            familiarHistory.remove(name);
            familiarHistory.add(name);
            while (familiarHistory.size() > FAMILIAR_HISTORY_MAX) familiarHistory.remove(0);
            try { java.nio.file.Files.write(familiarHistoryFile().toPath(), familiarHistory); }
            catch (Exception ignored) {}
        }
    }

    private File nameHistoryFile() { return new File(gdamDir, ".name_history"); }

    /** 배역 이름 사용 기록을 파일에서 불러온다(최근 NAME_HISTORY_MAX개 유지). */
    private void loadNameHistory() {
        File f = nameHistoryFile();
        if (!f.exists()) return;
        try {
            java.util.List<String> lines = java.nio.file.Files.readAllLines(f.toPath());
            synchronized (nameHistory) {
                nameHistory.clear();
                for (String l : lines) { String t = l.trim(); if (!t.isEmpty()) nameHistory.add(t); }
                while (nameHistory.size() > NAME_HISTORY_MAX) nameHistory.remove(0);
            }
        } catch (Exception ignored) {}
    }

    /** 사용한 배역 이름을 기록하고(최신화) 파일에 저장한다. 여러 개 한 번에. */
    private void recordNames(java.util.Collection<String> names) {
        if (names == null || names.isEmpty()) return;
        synchronized (nameHistory) {
            for (String name : names) {
                if (name == null || name.isBlank()) continue;
                String n = name.trim();
                nameHistory.remove(n);
                nameHistory.add(n);
            }
            while (nameHistory.size() > NAME_HISTORY_MAX) nameHistory.remove(0);
            try { java.nio.file.Files.write(nameHistoryFile().toPath(), nameHistory); }
            catch (Exception ignored) {}
        }
    }

    /** 최근 사용 배역 이름 목록(중복 회피용 CSV). */
    private String recentNameCsv() {
        synchronized (nameHistory) {
            return nameHistory.isEmpty() ? "" : String.join(", ", nameHistory);
        }
    }

    /** 최근 사용 괴담 목록(중복 회피용 CSV). */
    private String recentFamiliarCsv() {
        synchronized (familiarHistory) {
            return familiarHistory.isEmpty() ? "" : String.join(", ", familiarHistory);
        }
    }

