    private static String sample(String[] arr, int n) {
        Set<String> pick = new LinkedHashSet<>();
        int guard = 0;
        while (pick.size() < Math.min(n, arr.length) && guard++ < 100)
            pick.add(arr[ThreadLocalRandom.current().nextInt(arr.length)]);
        return String.join(" / ", pick);
    }

