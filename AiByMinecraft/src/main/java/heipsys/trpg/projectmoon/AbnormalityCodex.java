    /** 이름에 해당하는 3축 캐논 레코드 블록. 없으면 "" (전부 모델 자체 지식). */
    static String record(String name) {
        if (name == null || name.isEmpty()) return "";
        for (String[] r : CODEX) if (r[0].equals(name)) return format(r);
        return "";
    }

    private static String format(String[] r) {
        return "\n[상세 캐논 레코드 — GM 전용 · ★생김새·능력·강함만 고정★ · 원문/대사 복붙 금지]\n"
            + "· 피해유형 표기: 물리[RED]=몸 피해 / 정신[WHITE]=정신력 피해 / 침식[BLACK]=몸·정신 동시 잠식 / 영혼[PALE]=방어 무시 치명타.\n"
            + "· 생김새: " + r[1] + "\n"
            + "· 보유 능력: " + r[2] + "\n"
            + "· 강함: " + threatTier(r[3]) + " · " + r[3] + "\n"
            + "· ★해결·약점·관리법은 위에 없다 — 네가 이 환상체의 정전 설정에서 스스로 조사·수집해 반영하되, "
            + "플레이어에겐 탐색·단서로만 공개하라(한 번에 노출 금지). 위 생김새 묘사도 조금씩 드러내라.\n";
    }

    /**
     * ★위협도(1~6)★ — 등급으로 절대 위력을 매겨 강함 앞에 붙인다.
     * 저항 표현('전 속성 견딤' 등)이 상위 등급을 오히려 약해 보이게 만드는 역전을 막는 목적.
     * 등급이 곧 위력이므로 등급이 높을수록 위협도가 반드시 커진다(백야·종말새 등 최상위 초과 = 6).
     */
    private static String threatTier(String power) {
        int t; String label;
        if (power.contains("이상급") || power.contains("종말급")) { t = 6; label = "종말급(단일 알레프 초월)"; }
        else if (power.contains("ALEPH")) { t = 5; label = "최상위"; }
        else if (power.contains("WAW"))   { t = 4; label = "매우 강함"; }
        else if (power.contains("HE"))    { t = 3; label = "강함"; }
        else if (power.contains("TETH"))  { t = 2; label = "보통"; }
        else                              { t = 1; label = "약함"; }
        return "위협도 " + t + "/6(" + label + ")";
    }
